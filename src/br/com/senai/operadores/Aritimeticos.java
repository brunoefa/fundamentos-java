package br.com.senai.operadores;

public class Aritimeticos {

	public static void main(String[] args) {
		
		int a, b;
		
		// = Atribuição
		a = 10;
		b = 20;
		
		// + Adição
		a = a + b;
		
		// - Subtração
		b = b - 10;
		
		// * Multiplicação
		b = 5 * 5;
		
		// / Divisão
		a = b / 2;
		
		// % Resto da divisao
		a = b % 3;
		
		// ^ Elevado
		a = a^b;
		
		// ++ Pós incremento (a = a + 1;)
		a++;
	}

}
