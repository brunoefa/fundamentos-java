package br.com.senai.operadores;

public class Relacionais {

	public static void main(String[] args) {
		int a,b;
		boolean r;
		
		a = 10;
		b = 20;
		
		// > Maior que
		r = a > b;
		
		// < Menor que
		r = a < b;
		
		// >= Maior igual a
		r = a >= b;
		
		// <= Maior igual a
		r = a <= b;
		
		// != Diferente
		r = a != b;
		
		// == Igual
		r = a == b;
		
		System.out.println(r);
	}
	
}
